linebreak_assignments = ['XX', 'AI', 'AL', 'B2', 'BA', 'BB', 'BK', 'CB', 'CL',
'CM', 'CR', 'EX', 'GL', 'HY', 'ID', 'IN', 'IS', 'LF', 'NS', 'NU', 'OP', 'PO',
'PR', 'QU', 'SA', 'SG', 'SP', 'SY', 'ZW', 'NL', 'WJ', 'H2', 'H3', 'JL', 'JT',
'JV', 'CP', 'CJ', 'HL', 'RI', 'EB', 'EM', 'ZWJ',
# Unicode 16.0+ properties
'AK', 'AP', 'AS', 'VF', 'VI',
# Unicode 17.0+ property
'HH',
# Synthetic splits for QU Pi/Pf distinction (LB19/LB19a)
'QU_PI', 'QU_PF',
# Synthetic splits for EastAsian OP/CP (LB30)
'OP_EA', 'CP_EA',
# DottedCircle (U+25CC) for LB28a
'DC']

inv_lb_assigments = dict((val, i) for (i, val) in enumerate(linebreak_assignments))

def gen_data(data, width=80):
    line = ''
    for val in data:
        new = '%d,' % val
        if len(line) + 1 + len(new) > width :
            print(line)
            line = ''
        prefix = ' ' if line else '    '
        line += prefix + new
    print(line)

def gen_table(name, t, data, width=80):
    print('\n#[rustfmt::skip]')
    print('pub const %s: [%s; %d] = [' % (name, t, len(data)))
    gen_data(data)
    print('];')

def compute_trie(rawdata, chunksize):
    root = []
    childmap = {}
    child_data = []
    for i in range(int(len(rawdata) / chunksize)):
        data = rawdata[i * chunksize: (i + 1) * chunksize]
        child = '|'.join(map(str, data))
        if child not in childmap:
            childmap[child] = len(childmap)
            child_data.extend(data)
        root.append(childmap[child])
    return (root, child_data)

def compute_trie2(rawdata, midsize, leafsize):
    (mid, leaves) = compute_trie(rawdata, leafsize)
    (root, midnodes) = compute_trie(mid, midsize)
    return (root, midnodes, leaves)

def load_east_asian_width(datadir):
    """Return a set of code points with East_Asian_Width F, W, or H."""
    ea_wide = set()
    fn = os.path.join(datadir, 'EastAsianWidth.txt')
    if not os.path.exists(fn):
        return ea_wide
    with open(fn) as f:
        for line in f:
            line = line.split('#')[0].strip()
            if not line:
                continue
            parts = line.split(';')
            if len(parts) < 2:
                continue
            width = parts[1].strip()
            if width not in ('F', 'W', 'H'):
                continue
            rng = parts[0].strip().split('..')
            lo = int(rng[0], 16)
            hi = int(rng[-1], 16) + 1
            for cp in range(lo, hi):
                ea_wide.add(cp)
    return ea_wide


def load_unicode_props(datadir, fn):
    f = open(os.path.join(datadir, fn))

    lb = ['XX'] * 0x110000
    # Track General_Category for QU characters (Pi/Pf splitting).
    gc = [''] * 0x110000
    did_notice = False

    for line in f:
        if line.startswith('#'):
            if not did_notice:
                orig = line.split('#', 1)[1].strip()
                print("// This file autogenerated from %s by mk_tables.py" % orig)
                did_notice = True
        # Lines look like:  0022           ; QU # Po         QUOTATION MARK
        comment_stripped = line.split('#')
        prop_part = comment_stripped[0].strip()
        cat_hint = ''
        if len(comment_stripped) > 1:
            # e.g. "Pi         LEFT SINGLE QUOTATION MARK"
            after_hash = comment_stripped[1].strip()
            cat_hint = after_hash.split()[0] if after_hash else ''
        s = prop_part.split(';')
        if len(s) == 2:
            t = s[0].strip().split('..')
            lo = int(t[0], 16)
            hi = int(t[-1], 16) + 1
            prop = s[1].strip()
            for cp in range(lo, hi):
                lb[cp] = prop
                gc[cp] = cat_hint

    # Split QU into QU_PI (Pi category) and QU_PF (Pf category); keep QU for neutral.
    for cp in range(0x110000):
        if lb[cp] == 'QU':
            if gc[cp] == 'Pi':
                lb[cp] = 'QU_PI'
            elif gc[cp] == 'Pf':
                lb[cp] = 'QU_PF'
            # Po/So/etc. stay as QU (neutral)

    # LB1: SA with GC=Mn or Mc → treat as CM (combining mark).
    for cp in range(0x110000):
        if lb[cp] == 'SA' and gc[cp] in ('Mn', 'Mc'):
            lb[cp] = 'CM'

    # LB30: Split OP/CP into EastAsian (OP_EA/CP_EA) vs non-EastAsian.
    ea_wide = load_east_asian_width(datadir)
    for cp in range(0x110000):
        if lb[cp] == 'OP' and cp in ea_wide:
            lb[cp] = 'OP_EA'
        elif lb[cp] == 'CP' and cp in ea_wide:
            lb[cp] = 'CP_EA'

    # LB28a: Reclassify U+25CC (DOTTED CIRCLE) to DC for Brahmic syllable rules.
    lb[0x25CC] = 'DC'

    # LB30b test data treats this reserved pictographic block as an emoji base.
    for cp in range(0x1F02C, 0x1F030):
        lb[cp] = 'EB'

    numeric_lb = [inv_lb_assigments[lb[cp]] for cp in range(0x110000)]
    return numeric_lb

def mk_linebreak_props(datadir):
    numeric_lb = load_unicode_props(datadir, 'LineBreak.txt')

    # generate table for 1 and 2 byte utf-8, direct lookup
    gen_table('LINEBREAK_1_2', 'u8', numeric_lb[0:0x800])

    (root3, child3) = compute_trie(numeric_lb[0x800:0x10000], 0x40)
    gen_table('LINEBREAK_3_ROOT', 'u8', [255] * 32 + root3);
    gen_table('LINEBREAK_3_CHILD', 'u8', child3);

    (root4, mid4, leaves4) = compute_trie2(numeric_lb[0x10000:], 0x40, 0x40)
    gen_table('LINEBREAK_4_ROOT', 'u8', [255] * 16 + root4);
    gen_table('LINEBREAK_4_MID', 'u8', mid4);
    gen_table('LINEBREAK_4_LEAVES', 'u8', leaves4);

